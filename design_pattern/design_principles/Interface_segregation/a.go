package Interface_segregation

//Interface_segregation（Interface Segregation Principle, ISP）指用多个专门的接口，而不使用单一的总接口，客户端不应该依赖它不需要的接口。设计接口时，应当注意以下几点:
//（1）一个类对另一个类的依赖应该建立在最小接口上。
//（2）建立单一接口，不要建立庞大臃肿的接口。
//（3）尽量细化接口，接口中的方法尽量少。

// 总结：不要对外暴露没有实际意义的接口。

//使用多个隔离的接口，比使用单个接口要好。
//它还有另外一个意思是：降低类之间的耦合度。
//由此可见，其实设计模式就是从大型软件架构出发、便于升级和维护的软件设计思想，它强调降低依赖，降低耦合。

